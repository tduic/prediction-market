"""
Event type definitions for the prediction market trading system.
All events are immutable dataclasses for type safety and performance.
"""

from dataclasses import dataclass
from datetime import datetime, timezone
from typing import Any


@dataclass(frozen=True)
class MarketUpdated:
    """Fired when market price data is updated."""

    market_id: str
    platform: str
    yes_price: float | None = None
    no_price: float | None = None
    spread: float | None = None
    volume_24h: float | None = None
    open_interest: float | None = None
    liquidity: float | None = None
    poll_latency_ms: int | None = None
    timestamp: datetime = None

    def __post_init__(self):
        """Set timestamp if not provided."""
        if self.timestamp is None:
            object.__setattr__(self, "timestamp", datetime.now(timezone.utc))


@dataclass(frozen=True)
class ViolationDetected:
    """Fired when a constraint violation is detected (arbitrage opportunity)."""

    violation_id: str
    pair_id: str
    market_id_a: str
    market_id_b: str
    platform_a: str
    platform_b: str
    violation_type: str  # e.g., 'spread_violation', 'price_mismatch'
    price_a: float
    price_b: float
    raw_spread: float
    net_spread: float
    fee_estimate_a: float
    fee_estimate_b: float
    timestamp: datetime = None

    def __post_init__(self):
        """Set timestamp if not provided."""
        if self.timestamp is None:
            object.__setattr__(self, "timestamp", datetime.now(timezone.utc))


@dataclass(frozen=True)
class SignalFired:
    """Fired when a trading signal is generated by the system."""

    signal_id: str
