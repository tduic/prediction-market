    strategy: str  # e.g., 'constraint_arb', 'event_model', 'liquidity_rebate'
    signal_type: str  # e.g., 'market_pair', 'single_market'
    market_id_a: str
    market_id_b: str | None
    target_price_a: float | None
    target_price_b: float | None
    model_fair_value: float | None
    model_edge: float
    kelly_fraction: float
    position_size_a: float
    position_size_b: float | None
    total_capital_at_risk: float
    sizing_method: str  # e.g., 'kelly', 'fixed_pct', 'exposure_limit'
    timestamp: datetime = None

    def __post_init__(self):
        """Set timestamp if not provided."""
        if self.timestamp is None:
            object.__setattr__(self, "timestamp", datetime.now(timezone.utc))


